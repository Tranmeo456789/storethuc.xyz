     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Execute the job.
     *
     * @return void
     */
    public function handle()
    {
        $data=[];
        $email = new MailToAdmin($data);
        Mail::to('kieptuattuat@gmail.com')->send($email);
    }
}
